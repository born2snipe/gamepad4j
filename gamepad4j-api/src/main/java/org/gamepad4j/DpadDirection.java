package org.gamepad4j;

/**
 * Enumerations of all the possible directions of a D-Pad.
 *
 * @author Marcel Schoen
 * @version $Revision: $
 */
public enum DpadDirection {

	UP,
	UP_RIGHT,
	RIGHT,
	DOWN_RIGHT,
	DOWN,
	DOWN_LEFT,
	LEFT,
	UP_LEFT,
	NONE
	;
}
